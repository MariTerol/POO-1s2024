package aulas;

public class Calculadora {

    Double numA;
    Double numB;

    Double somar(Double numA, Double numB){

        return numA + numB;

    }

    Double subtrair(Double numA, Double numB){

        return numA - numB;

    }

    Double multiplicar(Double numA, Double numB){

        return numA * numB;

    }

    Double dividir(Double numA, Double numB){

        return numA / numB;

    }

    
}
